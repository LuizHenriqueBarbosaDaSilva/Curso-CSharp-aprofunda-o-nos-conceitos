using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Curso2.ClassesCurso
{
    public class MetodosStaticosExemplos
    {
        public static double _pi = 3.14;
        public static double Circuferencia(double _raio)
        {
            return 2.0 * _pi * _raio; 
        }
        public static double Volume(double _r)
        {
            return 2.0 * _pi *_r;
        }
    }
}
